<template>
  <el-select :disabled="disabled" :loading="loading" :multiple="multiple"
             ref="sel"
             @change="change" filterable v-model="value" :size="size">
    <el-option
      :key="item.code"
      :label="item.value1"
      :value="item.code"
      v-for="item in options">
    </el-option>
  </el-select>
</template>

<script>

    export default {
    name: 'dicselect',
    data() {
      return {
        options: [],
        value: '',
        loading: false
      }
    },
    props: {
      size:{
        type:String,

      },
      code: {
        type: String,
        default:
          function () {
            return ''
          }
      },
      multiple: {
        type: Boolean,
        default: false
      },
      data: {
        type: [String, Array]
      },
      disabled: {
        type: Boolean,
        default:
          false
      },
      no: {
        type: Object,
        default: function () {
          return {};
        }
      }
    },
    mounted() {
      if (this.data) {
        this.value = this.data;
      }
      if (this.code) {
        this.loading = true;
        this.options=[]
        let dic = this.$store.getters.dictionaryList.filter(item => item.pcode === this.code);
        for(let item of dic){
          this.options.push(item);
        }
        this.loading = false;
        // this.$store
        //   .dispatch('dictionaryStore/getForSelect', {'code': this.code})
        //   .then(response => {
        //     this.options = response;
        //     this.loading = false;
        //   })
        //   .catch(error => {
        //     this.loading = false;
        //     Message({
        //       message: error,
        //       type: 'error',
        //       duration: 5 * 1000
        //     })
        //   })
      }else{
        this.options=[]
      }
    },
    methods: {
      change(val) {
        this.$emit("change", val, this.no);
      }
    },
    watch: {
      data(val) {
        if (val) {
          this.value = this.data;
        } else {
            this.value = '';
        }
      },
      code(val) {
        if (val) {
          this.options=[];
          let dic = this.$store.getters.dictionaryList.filter(item => item.pcode === val);
          for(let item of dic){
            this.options.push(item);
          }
        }else{
          this.options=[];
        }
      }
    }
  }
</script>

